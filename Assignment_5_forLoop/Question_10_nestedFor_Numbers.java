package assignments.Assignment_5_forLoop;

public class Question_10_nestedFor_Numbers {

	public static void main(String[] args) {
		
		for(int i=1; i<=7; i++) {
			
			for(int j=1; j<=i; j++) {
				
				System.out.print(i);
				
			}
			System.out.println();
			
		}
		
		
		
		
		
		
		
		

	}

}
